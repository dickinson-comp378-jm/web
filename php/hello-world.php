<?php

// This file demonstrates how standard HTML can be produced using the
// PHP `echo` statement

// plain text
echo "hello world\n";

// line breaks
echo "<br>line 1<br>line 2<br>line 3<br>\n";

// bold
echo "<b>hello bold world</b>\n";

// heading
echo "<h2>Hello heading</h2>\n";

// table
echo "Hello table:";

echo "<table border='1'>\n";
echo "  <tr><th>Col 1</th><th>Col 2</th><th>Col 3</th></tr>\n";
echo "  <tr><td>val 1</td><td>val 2</td><td>val 3</td></tr>\n";
echo "  <tr><td>val 1b</td><td>val 2b</td><td>val 3b</td></tr>\n";
echo "</table>\n";


?>
